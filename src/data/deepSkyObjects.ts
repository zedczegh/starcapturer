export const searchDeepSkyObjects = (query: string): DeepSkyObject[] => {
  if (!query) return DEEP_SKY_OBJECTS.slice(0, 50); // Show top 50 by default
  
  const lowerQuery = query.toLowerCase();
  return DEEP_SKY_OBJECTS.filter(obj => 
    obj.value.toLowerCase().includes(lowerQuery) ||
    obj.label.toLowerCase().includes(lowerQuery) ||
    obj.type.toLowerCase().includes(lowerQuery) ||
    obj.constellation?.toLowerCase().includes(lowerQuery)
  ).slice(0, 100); // Limit to 100 results
};
